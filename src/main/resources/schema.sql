CREATE TABLE IF NOT EXISTS users (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name VARCHAR(255) NOT NULL,
  email VARCHAR(512) NOT NULL,
  CONSTRAINT UQ_USER_EMAIL UNIQUE (email)
);

CREATE TABLE IF NOT EXISTS items (
id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
name VARCHAR(255) NOT NULL,
description VARCHAR(255),
available BOOLEAN,
user_id BIGINT NOT NULL,
request_id BIGINT,
booking_id BIGINT,
CONSTRAINT fk_user FOREIGN KEY (user_id) REFERENCES users(id),
CONSTRAINT fk_request FOREIGN KEY (request_id) REFERENCES requests(id),
CONSTRAINT fk_booking FOREIGN KEY (booking_id) REFERENCES bookings(id)
);

CREATE TYPE booking_status AS ENUM ('WAITING','APPROVED','REJECTED','CANCELED');

CREATE TABLE IF NOT EXISTS bookings (
id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
started DATE,
ended DATE,
item_id BIGINT NOT NULL,
booker_id BIGINT NOT NULL,
status booking_status,
CONSTRAINT fk_item_id FOREIGN KEY (item_id) REFERENCES items(id),
CONSTRAINT fk_booker_id FOREIGN KEY (booker_id) REFERENCES users(id)
);

CREATE TABLE IF NOT EXISTS requests (
id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
description VARCHAR(300),
created DATE,
requestor_id BIGINT,
CONSTRAINT fk_requestor FOREIGN KEY (requestor_id) REFERENCES users(id)
);

